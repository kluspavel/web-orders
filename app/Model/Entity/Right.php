<?php declare(strict_types = 1);

namespace App\Model\Entity;

use App\Model\Entity\Attributes\TraitId;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity]
#[ORM\Table(name: 'rights')]
class Right extends Entity
{
    //--------------------------------------------------------------------------------------------------------
	use TraitId;
	//--------------------------------------------------------------------------------------------------------
    #[ORM\Column(type: 'integer', length: 32, nullable: false, unique: true)]
	private $data;
    //--------------------------------------------------------------------------------------------------------
    #[ORM\Column(type: 'integer', length: 8, nullable: false, unique: true)]
	private $right;
    //--------------------------------------------------------------------------------------------------------
    #[ORM\Column(type: 'string', length: 32, nullable: false, unique: true)]
	private $name;
    //--------------------------------------------------------------------------------------------------------
    #[ORM\Column(type: 'string', length: 1024, nullable: false)]
	private $description;
    //--------------------------------------------------------------------------------------------------------
    // CONSTRUCT
    //--------------------------------------------------------------------------------------------------------
	public function __construct() {}
	//--------------------------------------------------------------------------------------------------------
	public function incejtData(int $data, int $right, string $name, string $description): void
	{
		$this->data = $data;
		$this->right = $right;
		$this->name = $name;
        $this->description = $description;
	}
    //--------------------------------------------------------------------------------------------------------
    // GET SET type
    //--------------------------------------------------------------------------------------------------------
	public function getData(): int
	{
		return $this->data;
	}
	//--------------------------------------------------------------------------------------------------------
	public function setData(int $data): void
	{
		$this->data = $data;
	}
    //--------------------------------------------------------------------------------------------------------
    // GET SET type
    //--------------------------------------------------------------------------------------------------------
	public function getType(): int
	{
		return $this->right;
	}
	//--------------------------------------------------------------------------------------------------------
	public function setType(int $right): void
	{
		$this->right = $right;
	}
    //--------------------------------------------------------------------------------------------------------
    // GET SET name
    //--------------------------------------------------------------------------------------------------------
	public function getName(): string
	{
		return $this->name;
	}
	//--------------------------------------------------------------------------------------------------------
	public function setName(string $name): void
	{
		$this->name = $name;
	}
    //--------------------------------------------------------------------------------------------------------
    // GET SET description
    //--------------------------------------------------------------------------------------------------------
	public function getDescription(): string
	{
		return $this->description;
	}
	//--------------------------------------------------------------------------------------------------------
	public function setDescription(string $description): void
	{
		$this->description = $description;
	}
}