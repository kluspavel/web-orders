<?php declare(strict_types = 1);

namespace App\Model\Entity;

use App\Model\Entity\Attributes\TraitId;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity]
#[ORM\Table(name: 'types')]
class Type extends Entity
{
    //--------------------------------------------------------------------------------------------------------
	use TraitId;
	//--------------------------------------------------------------------------------------------------------
    #[ORM\Column(type: 'integer', length: 16, nullable: false, unique: true)]
	private $data;
    //--------------------------------------------------------------------------------------------------------
    #[ORM\Column(type: 'integer', length: 8, nullable: false, unique: true)]
	private $type;
    //--------------------------------------------------------------------------------------------------------
    #[ORM\Column(type: 'string', length: 32, nullable: false, unique: true)]
	private $name;
    //--------------------------------------------------------------------------------------------------------
    // CONSTRUCT
    //--------------------------------------------------------------------------------------------------------
	public function __construct() {}
	//--------------------------------------------------------------------------------------------------------
	public function incejtData(int $data, int $type, string $name): void
	{
		$this->data = $data;
		$this->type = $type;
		$this->name = $name;
	}
    //--------------------------------------------------------------------------------------------------------
    // GET SET type
    //--------------------------------------------------------------------------------------------------------
	public function getData(): int
	{
		return $this->data;
	}
	//--------------------------------------------------------------------------------------------------------
	public function setData(int $data): void
	{
		$this->data = $data;
	}
    //--------------------------------------------------------------------------------------------------------
    // GET SET type
    //--------------------------------------------------------------------------------------------------------
	public function getType(): int
	{
		return $this->type;
	}
	//--------------------------------------------------------------------------------------------------------
	public function setType(int $type): void
	{
		$this->type = $type;
	}
    //--------------------------------------------------------------------------------------------------------
    // GET SET name
    //--------------------------------------------------------------------------------------------------------
	public function getName(): string
	{
		return $this->name;
	}
	//--------------------------------------------------------------------------------------------------------
	public function setName(string $name): void
	{
		$this->name = $name;
	}
}